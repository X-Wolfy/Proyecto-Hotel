CREATE TABLE HABITACIONES(
    ID_HABITACION NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NUMERO NUMBER(4) NOT NULL,
    TIPO VARCHAR2(30) NOT NULL,
    PRECIO NUMBER(10,2) NOT NULL,
    CAPACIDAD NUMBER(2) NOT NULL,
    ESTADO_HABITACION VARCHAR2(30) NOT NULL,
    ESTADO_REGISTRO VARCHAR2(30) NOT NULL,
    
    PRIMARY KEY(ID_HABITACION),
    
    CHECK (ESTADO_HABITACION IN ('DISPONIBLE','OCUPADA','LIMPIEZA','MANTENIMIENTO')),
    CHECK (ESTADO_REGISTRO IN ('ACTIVO','ELIMINADO')),
    CHECK (NUMERO > 0),
    CHECK (PRECIO > 0),
    CHECK (CAPACIDAD >= 1)
);

CREATE TABLE HUESPEDES(
    ID_HUESPED NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(50) NOT NULL,
    APELLIDO_PATERNO VARCHAR2(50) NOT NULL,
    APELLIDO_MATERNO VARCHAR2(50) NOT NULL,
    EDAD NUMBER(3,0) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL,
    TELEFONO VARCHAR2(10) NOT NULL,
    DOCUMENTO VARCHAR2(30) NOT NULL,
    NACIONALIDAD VARCHAR2(30) NOT NULL,
    ESTADO_REGISTRO VARCHAR2(30) NOT NULL,

    PRIMARY KEY(ID_HUESPED),

    CHECK (ESTADO_REGISTRO IN ('ACTIVO','ELIMINADO')),

    CHECK (EDAD BETWEEN 18 AND 100)
);

CREATE TABLE RESERVACIONES (
    ID_RESERVACION NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_HABITACION NUMBER NOT NULL,
    ID_HUESPED NUMBER NOT NULL,
    FECHA_ENTRADA DATE NOT NULL,
    FECHA_SALIDA DATE NOT NULL,
    ESTADO_REGISTRO VARCHAR2(30) NOT NULL,
    ESTADO_RESERVA VARCHAR2(30),
    
    PRIMARY KEY(ID_RESERVACION),
    
    CHECK (FECHA_ENTRADA < FECHA_SALIDA),
    
    CHECK (ESTADO_REGISTRO IN ('ACTIVO','ELIMINADO')),
    
    CHECK (ESTADO_RESERVA IN ('CONFIRMADA','EN_CURSO', 'FINALIZADA', 'CANCELADA'))
);

CREATE TABLE usuarios (
    ID_USUARIO NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    username VARCHAR2(20) NOT NULL,
    password VARCHAR2(255) NOT NULL,
    rol VARCHAR2(10) NOT NULL CHECK (rol IN ('ADMIN', 'USER')),
    estado_registro VARCHAR2(10) DEFAULT 'ACTIVO' NOT NULL CHECK (estado_registro IN ('ACTIVO', 'ELIMINADO')),

    CONSTRAINT chk_usuarios_username_len CHECK (LENGTH(username) >= 5)
);

CREATE UNIQUE INDEX uq_usuarios_username ON usuarios (
    CASE WHEN estado_registro = 'ACTIVO' THEN username ELSE NULL END
);
